           <p class="bold">About</p>
         </div>
        <p>
        Hayme: A Unique Digital Art Generator is a revolutionary app designed for artists who want to streamline their creative process and automatically generate multiple or even thousands of unique digital art pieces using their own pre-made traits. With this app, users can harness the power of their own creative vision to generate an array of diverse art pieces, making the art generation process efficient and effortless.
        </p>
        <br>
        <div class="title">
           <p class="bold">Creator</p>
         </div>
        <p>
        James Bago is a digital artist with over 6 years of experience in painting, illustrations, and character designs. While his programming knowledge may be foundational, James's expertise lies in crafting captivating visual experiences. Fuelled by a relentless drive for innovation, he has successfully developed the Hayme: A Unique Digital Art Generator—an app that merges pre-made character traits of a subject to generate digital art based on the user's desired quantity input. The app also generates metadata from the file names of each pre-made trait and creates a 2D digital art from these pre-made traits. James's goal as an artist and innovator is to inspire and captivate audiences through his digital creations.
        </p>
